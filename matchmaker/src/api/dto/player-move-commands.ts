import { Hex } from "./player-response"; // Импорт Hex из предыдущего файла

/**
 * Команда перемещения для одного муравья.
 */
export interface AntMoveCommand {
  /**
   * ID муравья, которого нужно переместить.
   * Пример: "11111111-2222-3333-4444-555555555555"
   */
  ant: string;

  /**
   * Путь из последовательности гексов (Hex), по которым муравей должен двигаться.
   * Каждый гекс задаётся координатами { q, r }.
   */
  path: Hex[];
}

/**
 * Общий набор команд для перемещения муравьёв.
 * Используется в теле POST-запроса к /api/move.
 */
export interface PlayerMoveCommands {
  /**
   * Массив команд для муравьёв.
   * Если массив пуст, муравьи остаются на месте.
   */
  moves: AntMoveCommand[];
}
